function tokenise(text) {
  return text
    .toLowerCase()
    .replace(/[^\w\s'-]/g, " ")
    .split(/\s+/)
    .filter(Boolean);
}

function prefix(text, words) {
  return tokenise(text).slice(0, words).join(" ");
}

export function validateCategoryMessages(texts, category) {
  const errors = [];

  if (texts.length !== 100) {
    errors.push(`${category}: expected 100 messages, got ${texts.length}`);
  }

  const unique = new Set(texts);
  if (unique.size !== texts.length) {
    errors.push(`${category}: contains ${texts.length - unique.size} duplicate message(s)`);
  }

  const prefixCounts = new Map();
  for (const text of texts) {
    const key = prefix(text, 2);
    prefixCounts.set(key, (prefixCounts.get(key) ?? 0) + 1);
  }

  for (const [key, count] of prefixCounts) {
    if (count > 1) {
      errors.push(`${category}: repeated opening "${key}..." (${count} times)`);
    }
  }

  const wordCounts = new Map();
  for (const text of texts) {
    for (const word of tokenise(text)) {
      if (STOP_WORDS.has(word) || word.length < 4) continue;
      wordCounts.set(word, (wordCounts.get(word) ?? 0) + 1);
    }
  }

  const overused = [...wordCounts.entries()]
    .filter(([, count]) => count > 4)
    .sort((a, b) => b[1] - a[1]);

  for (const [word, count] of overused) {
    errors.push(`${category}: word "${word}" appears ${count} times (max 4)`);
  }

  return errors;
}

export function validateAllMessages(allMessages) {
  return Object.entries(allMessages).flatMap(([category, texts]) =>
    validateCategoryMessages(texts, category),
  );
}
